/**
 * A simple Java program that creates a UserInterface object and calls its Start_System method.
 * This program demonstrates object-oriented programming concepts and the use of the main method as an entry point.
 */
public class Main {

    /**
     * Entry point for the program. Creates a UserInterface object and calls its Start_System method.
     * @param args command-line arguments (not used in this program)
     */
    public static void main(String[] args) {

        /** Create an instance of the UserInterface class */
        UserInterface userInterface = new UserInterface();

        /** Call the Start_System method of the UserInterface object to start the system */
        userInterface.Start_System();

    }
}





